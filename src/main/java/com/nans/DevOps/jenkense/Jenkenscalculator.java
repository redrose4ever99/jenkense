package com.nans.DevOps.jenkense;

public class Jenkenscalculator {
	
	public int addNum(int n1,int n2) {
		return n1+n2;
	}
	
	public int subNum(int n1,int n2) {
		return n1-n2;
	}
	
	public int subMethod(int n1,int n2) {
		return n1-n2;
	}
	
	public int addMethod(int n1,int n2) {

		return n1+n2;
	}

}
